def stemming(all_words):
    # all_words = [stem(w) for w in all_words if w not in ignore_words]
    stemmed_word = []
    for w in all_words:
        for rules in step1a_set:
            suffix, replace = rules
            if w.endswith(suffix):
                w = w[:-len(suffix)] + replace
        stemmed_word.append(w)
    return stemmed_word
